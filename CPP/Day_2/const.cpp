//? CONSTANT value in C++ using "const" keyword.

#include <iostream>
using namespace std;

int main()
{
    const float PI = 3.14;
    //* PI = 3.41; it will give you and error because we can't change PI's value.

    cout << "Value of PI is " << PI;
    return 0;
}

//! Note:- We can use "const" 'KEYWORD' & "macro" 'FUNCTION' for Constant Values.